export function tFor(locale: Locale) {
  return (key: StringKey, params?: Record<string, string>): string => {
    const row = STRINGS[key];
    let s = row[locale] ?? row.en;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        s = s.replace(`{${k}}`, v);
      }
    }
    return s;
  };
}
