export function resolveLocale(raw: string | string[] | undefined): Locale {
  if (!raw) return "en";
  const v = (Array.isArray(raw) ? raw[0] : raw) ?? "en";
  const lc = String(v).toLowerCase().slice(0, 5).split(/[-_]/)[0] ?? "en";
  return (LOCALES as readonly string[]).includes(lc) ? (lc as Locale) : "en";
}

